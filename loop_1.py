'''
range(10)
- loop start from 0 and finish at 9
- step will be 1
'''
for i in range(10):
    print(i)

print("==========================")

'''
range(10,100)
- loop start from 10 and finish at 99
- step will be 1
'''
for i in range(10,100):
    print(i)

print("==========================")

'''
range(10,100)
- loop start from 10 and finish at 99
- step will be 10
'''
for i in range(10,101,10):
    print(i)
